package examples;

public class AdditionOfTwoNumbers {
    public int addTwoNumbers(int num1, int num2){
        return num1 + num2;
    }

    public int addTwoNumbers(int number1, double number2){
        int sum = (int) (number1 + number2);
        return sum;
    }

    public double addTwoNumbers(double num1, double num2){
        return num1 + num2;
    }

    public double addTwoNumbers(double num1, int num2){
        return num1 + num2;
    }
}
